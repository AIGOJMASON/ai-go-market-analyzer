"""
stage_contractor_router_probe.py

Purpose:
- Exercise the contractor_builder_v1 router API surface in isolation
- Verify that the router branch can:
  - create a real project
  - initialize workflow
  - persist lawful schedule blocks
  - detect schedule conflicts
  - build capacity posture
  - classify cascade risk
  - emit router receipts

Usage examples:
    python -m AI_GO.tests.stage_contractor_router_probe
    python AI_GO/tests/stage_contractor_router_probe.py

Notes:
- This is a probe, not a mocked unit test.
- It mounts only the contractor_builder_v1 router into a local FastAPI app.
- It uses a real project and real workflow phases, then overlays schedule blocks
  that intentionally create conflict and capacity pressure.
"""

