def _assert_file_exists(path_str: str, label: str) -> Path:
    path = Path(path_str)
    if not path.exists():
        raise AssertionError(f"Expected {label} to exist, but it was missing: {path}")
    return path


def _load_json(path: Path, label: str) -> Any:
    try:
        with path.open("r", encoding="utf-8") as handle:
            return json.load(handle)
    except Exception as exc:
        raise AssertionError(f"Failed to load {label} JSON from {path}: {exc}") from exc


def _verify_blocks_persisted(
    *,
    project_id: str,
    blocks: List[Dict[str, Any]],
    blocks_response: Dict[str, Any],
) -> tuple[Path, Path]:
    blocks_path = _assert_file_exists(
        blocks_response.get("artifact_path", ""),
        "router schedule blocks artifact",
    )
    payload = _load_json(blocks_path, "router schedule blocks artifact")

    if payload.get("project_id") != project_id:
        raise AssertionError(
            f"schedule_blocks project_id mismatch: {payload.get('project_id')} != {project_id}"
        )

    if int(payload.get("block_count", -1)) != len(blocks):
        raise AssertionError(
            f"schedule_blocks block_count mismatch: {payload.get('block_count')} != {len(blocks)}"
        )

    persisted_blocks = payload.get("blocks", [])
    if len(persisted_blocks) != len(blocks):
        raise AssertionError(
            f"persisted blocks length mismatch: {len(persisted_blocks)} != {len(blocks)}"
        )

    receipt_path = _assert_file_exists(
        blocks_response.get("receipt_path", ""),
        "router store_schedule_blocks receipt",
    )
    receipt = _load_json(receipt_path, "router store_schedule_blocks receipt")

    if receipt.get("module_id") != "router":
        raise AssertionError(f"router receipt module_id mismatch: {receipt.get('module_id')}")

    if receipt.get("event_type") != "store_schedule_blocks":
        raise AssertionError(f"router receipt event_type mismatch: {receipt.get('event_type')}")

    if receipt.get("project_id") != project_id:
        raise AssertionError(
            f"router receipt project_id mismatch: {receipt.get('project_id')} != {project_id}"
        )

    return blocks_path, receipt_path


def _verify_router_analysis(
    *,
    project_id: str,
    analyze_response: Dict[str, Any],
) -> None:
    conflicts = analyze_response.get("conflicts", [])
    capacity_records = analyze_response.get("capacity_records", [])
    cascade = analyze_response.get("cascade_risk", {})
    receipt = analyze_response.get("receipt", {})

    if not conflicts:
        raise AssertionError("Expected at least one router conflict")

    conflict_types = {str(conflict.get("conflict_type", "")) for conflict in conflicts}
    if "Crew_Double_Booked" not in conflict_types:
        raise AssertionError(f"Expected Crew_Double_Booked conflict, got: {conflict_types}")

    if "Dependency_Violation" not in conflict_types:
        raise AssertionError(f"Expected Dependency_Violation conflict, got: {conflict_types}")

    if not capacity_records:
        raise AssertionError("Expected at least one capacity record")

    shared_crew_records = [
        record for record in capacity_records
        if record.get("resource_name") == "Shared Crew Alpha"
    ]
    if not shared_crew_records:
        raise AssertionError("Expected capacity record for Shared Crew Alpha")

    if shared_crew_records[0].get("capacity_status") != "Overloaded":
        raise AssertionError(
            f"Expected Shared Crew Alpha capacity_status Overloaded, got: {shared_crew_records[0].get('capacity_status')}"
        )

    if cascade.get("project_id") != project_id:
        raise AssertionError(
            f"cascade_risk project_id mismatch: {cascade.get('project_id')} != {project_id}"
        )

    if str(cascade.get("cascade_risk_label", "")).strip() not in {"moderate", "high"}:
        raise AssertionError(
            f"Expected cascade_risk_label moderate/high, got: {cascade.get('cascade_risk_label')}"
        )

    if int(cascade.get("conflict_count", 0)) < 2:
        raise AssertionError(
            f"Expected conflict_count >= 2, got: {cascade.get('conflict_count')}"
        )

    if receipt.get("module_id") != "router":
        raise AssertionError(f"analysis receipt module_id mismatch: {receipt.get('module_id')}")

    if receipt.get("event_type") != "run_conflict_scan":
        raise AssertionError(f"analysis receipt event_type mismatch: {receipt.get('event_type')}")

    if receipt.get("project_id") != project_id:
        raise AssertionError(
            f"analysis receipt project_id mismatch: {receipt.get('project_id')} != {project_id}"
        )


def run_probe() -> Dict[str, Any]:
    app = _build_isolated_app()
    client = TestClient(app)

    project_payload = _build_project_create_payload()
    project_response = _post_create_project(client, project_payload)

    project_id = project_response.get("project_id")
    if not project_id:
        raise AssertionError(f"Missing project_id in project create response: {project_response}")

    workflow_response = _post_initialize_workflow(
        client,
        project_id=project_id,
        phase_templates=_build_phase_templates(),
    )

    phase_instances = workflow_response.get("phase_instances", [])
    if len(phase_instances) < 2:
        raise AssertionError("Workflow initialize did not return enough phase instances for router probe")

    blocks = _build_schedule_blocks(project_id, phase_instances)

    blocks_response = _post_persist_blocks(
        client,
        project_id=project_id,
        blocks=blocks,
    )
    blocks_path, blocks_receipt_path = _verify_blocks_persisted(
        project_id=project_id,
        blocks=blocks,
        blocks_response=blocks_response,
    )

    analyze_response = _post_router_analyze(
        client,
        project_id=project_id,
        blocks=blocks,
    )
    _verify_router_analysis(
        project_id=project_id,
        analyze_response=analyze_response,
    )

    summary = {
        "probe": "stage_contractor_router_probe",
        "mode": "isolated_router_only",
        "status": "passed",
        "project_id": project_id,
        "verified_paths": {
            "schedule_blocks": str(blocks_path),
            "store_schedule_blocks_receipt": str(blocks_receipt_path),
        },
        "response_summary": {
            "blocks_status": blocks_response.get("status"),
            "analysis_status": analyze_response.get("status"),
            "project_name": project_payload["project_name"],
            "conflict_count": len(analyze_response.get("conflicts", [])),
            "capacity_record_count": len(analyze_response.get("capacity_records", [])),
            "cascade_risk_label": analyze_response.get("cascade_risk", {}).get("cascade_risk_label"),
        },
    }
    return summary


def main() -> None:
    try:
        result = run_probe()
        print(json.dumps(result, indent=2, ensure_ascii=False))
        print("STAGE_CONTRACTOR_ROUTER_PROBE: PASS")
    except Exception as exc:
        print(f"STAGE_CONTRACTOR_ROUTER_PROBE: FAIL\n{exc}", file=sys.stderr)
        raise


if __name__ == "__main__":
    main()