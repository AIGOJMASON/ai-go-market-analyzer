from __future__ import annotations

"""
stage_contractor_oracle_probe.py

Purpose:
- Exercise the contractor_builder_v1 oracle API surface in isolation
- Verify that the oracle branch can:
  - create a real project
  - list bounded registered snapshots
  - publish a known snapshot through the API flow
  - classify a shock record
  - build a project exposure profile
  - build an oracle projection
  - build a risk radar
  - build a procurement advisory
  - emit an oracle receipt

Usage examples:
    python -m AI_GO.tests.stage_contractor_oracle_probe
    python AI_GO/tests/stage_contractor_oracle_probe.py

Notes:
- This is a probe, not a mocked unit test.
- It mounts only the contractor_builder_v1 router into a local FastAPI app.
- It uses the bounded in-repo oracle registry, not live market data.
"""

import json
import sys
from datetime import UTC, datetime
from pathlib import Path
from typing import Any, Dict, List

from fastapi import FastAPI
from fastapi.testclient import TestClient


def _build_isolated_app() -> FastAPI:
    try:
        from AI_GO.api.contractor_builder_api import router as contractor_builder_router
    except Exception as exc:
        raise RuntimeError(
            "Unable to import isolated contractor builder router: "
            f"{exc}"
        ) from exc

    app = FastAPI(title="contractor_builder_v1_oracle_probe_app")
    app.include_router(contractor_builder_router)
    return app


def _now_compact() -> str:
    return datetime.now(UTC).strftime("%Y%m%dT%H%M%SZ")


