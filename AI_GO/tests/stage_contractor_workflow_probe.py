def _build_isolated_app() -> FastAPI:
    """
    Build a tiny isolated FastAPI app that mounts only the contractor router.
    """
    try:
        from AI_GO.api.contractor_builder_api import router as contractor_builder_router
    except Exception as exc:
        raise RuntimeError(
            "Unable to import isolated contractor builder router: "
            f"{exc}"
        ) from exc

    app = FastAPI(title="contractor_builder_v1_workflow_probe_app")
    app.include_router(contractor_builder_router)
    return app


