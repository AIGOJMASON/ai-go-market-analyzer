def _post_create_project(client: TestClient, payload: Dict[str, Any]) -> Dict[str, Any]:
    response = client.post("/contractor-builder/projects/create", json=payload)

    if response.status_code != 200:
        raise AssertionError(
            "Project create failed during workflow probe.\n"
            f"status_code={response.status_code}\n"
            f"response_text={response.text}"
        )

    data = response.json()
    if data.get("status") != "created":
        raise AssertionError(f"Expected project create status='created', got: {data}")

    return data


def _post_initialize_workflow(
    client: TestClient,
    project_id: str,
    phase_templates: List[Dict[str, Any]],
) -> Dict[str, Any]:
    response = client.post(
        "/contractor-builder/workflow/initialize",
        json={
            "project_id": project_id,
            "phase_templates": phase_templates,
            "overwrite": False,
        },
    )

    if response.status_code != 200:
        raise AssertionError(
            "Workflow initialize failed.\n"
            f"status_code={response.status_code}\n"
            f"response_text={response.text}"
        )

    data = response.json()
    if data.get("status") != "initialized":
        raise AssertionError(f"Expected workflow status='initialized', got: {data}")

    return data


def _assert_file_exists(path_str: str, label: str) -> Path:
    path = Path(path_str)
    if not path.exists():
        raise AssertionError(f"Expected {label} to exist, but it was missing: {path}")
    return path


def _load_json(path: Path, label: str) -> Any:
    try:
        with path.open("r", encoding="utf-8") as handle:
            return json.load(handle)
    except Exception as exc:
        raise AssertionError(f"Failed to load {label} JSON from {path}: {exc}") from exc


def _verify_workflow_files(
    project_id: str,
    workflow_response: Dict[str, Any],
    phase_templates: List[Dict[str, Any]],
) -> Tuple[Path, Path]:
    project_root = Path("AI_GO/state/contractor_builder_v1/projects/by_project") / project_id
    phase_instances_path = project_root / "workflow" / "phase_instances.json"
    phase_history_path = project_root / "workflow" / "phase_history.jsonl"

    _assert_file_exists(str(phase_instances_path), "phase instances")
    _assert_file_exists(str(phase_history_path), "phase history log")

    phase_instances = _load_json(phase_instances_path, "phase instances")
    if not isinstance(phase_instances, list):
        raise AssertionError("phase_instances.json should contain a list of phase instances")

    if len(phase_instances) != len(phase_templates):
        raise AssertionError(
            f"Phase instance count mismatch: {len(phase_instances)} != {len(phase_templates)}"
        )

    workflow_state = workflow_response.get("workflow_state", {})
    if workflow_state.get("project_id") != project_id:
        raise AssertionError(
            f"workflow_state.project_id mismatch: {workflow_state.get('project_id')} != {project_id}"
        )

    if int(workflow_state.get("phase_count", -1)) != len(phase_templates):
        raise AssertionError(
            f"workflow_state.phase_count mismatch: {workflow_state.get('phase_count')} != {len(phase_templates)}"
        )

    for instance in phase_instances:
        if instance.get("project_id") != project_id:
            raise AssertionError(
                f"phase instance project_id mismatch: {instance.get('project_id')} != {project_id}"
            )
        if instance.get("phase_status") != "not_started":
            raise AssertionError(
                f"phase instance should start as not_started, got: {instance.get('phase_status')}"
            )
        if not instance.get("phase_id"):
            raise AssertionError("phase instance missing phase_id")
        if not instance.get("template_id"):
            raise AssertionError("phase instance missing template_id")
        if not instance.get("phase_name"):
            raise AssertionError("phase instance missing phase_name")

    return phase_instances_path, phase_history_path


def _verify_workflow_receipt(
    project_id: str,
    workflow_response: Dict[str, Any],
    phase_templates: List[Dict[str, Any]],
) -> Path:
    receipt_path = _assert_file_exists(
        workflow_response.get("receipt_path", ""),
        "workflow create_phase_plan receipt",
    )
    receipt = _load_json(receipt_path, "workflow receipt")

    if receipt.get("module_id") != "workflow":
        raise AssertionError(
            f"workflow receipt module_id mismatch: {receipt.get('module_id')}"
        )

    if receipt.get("event_type") != "create_phase_plan":
        raise AssertionError(
            f"workflow receipt event_type mismatch: {receipt.get('event_type')}"
        )

    if receipt.get("project_id") != project_id:
        raise AssertionError(
            f"workflow receipt project_id mismatch: {receipt.get('project_id')} != {project_id}"
        )

    details = receipt.get("details", {})
    if int(details.get("phase_count", -1)) != len(phase_templates):
        raise AssertionError(
            f"workflow receipt phase_count mismatch: {details.get('phase_count')} != {len(phase_templates)}"
        )

    return receipt_path


def run_probe() -> Dict[str, Any]:
    app = _build_isolated_app()
    client = TestClient(app)

    project_payload = _build_project_create_payload()
    project_response = _post_create_project(client, project_payload)
    project_id = project_response.get("project_id")
    if not project_id:
        raise AssertionError(f"Missing project_id in project create response: {project_response}")

    phase_templates = _build_phase_templates()
    workflow_response = _post_initialize_workflow(client, project_id, phase_templates)

    phase_instances_path, phase_history_path = _verify_workflow_files(
        project_id=project_id,
        workflow_response=workflow_response,
        phase_templates=phase_templates,
    )
    workflow_receipt_path = _verify_workflow_receipt(
        project_id=project_id,
        workflow_response=workflow_response,
        phase_templates=phase_templates,
    )

    summary = {
        "probe": "stage_contractor_workflow_probe",
        "mode": "isolated_router_only",
        "status": "passed",
        "project_id": project_id,
        "verified_paths": {
            "phase_instances": str(phase_instances_path),
            "phase_history": str(phase_history_path),
            "workflow_receipt": str(workflow_receipt_path),
        },
        "response_summary": {
            "status": workflow_response.get("status"),
            "phase_count": workflow_response.get("workflow_state", {}).get("phase_count"),
            "project_name": project_payload["project_name"],
            "project_type": project_payload["project_type"],
        },
    }
    return summary


def main() -> None:
    try:
        result = run_probe()
        print(json.dumps(result, indent=2, ensure_ascii=False))
        print("STAGE_CONTRACTOR_WORKFLOW_PROBE: PASS")
    except Exception as exc:
        print(f"STAGE_CONTRACTOR_WORKFLOW_PROBE: FAIL\n{exc}", file=sys.stderr)
        raise


if __name__ == "__main__":
    main()